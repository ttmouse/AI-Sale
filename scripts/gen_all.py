def esc(t):
    """Escape text for JS string"""
    return t.replace("'", "\\'").replace('\n', '\\n')

def gen_scenario(s):
    msgs_str = ',\n        '.join(s['messages'])
    anns_str = ',\n        '.join(s['annotations'] if s['annotations'] else ['null'])
    hl_str = '[' + ','.join(f"'{esc(h)}'" for h in s['highlights']) + ']'
    return f'''    {{
      id: '{s['id']}', stageId: '{s['stageId']}',
      name: '{esc(s['name'])}',
      painPoint: '{esc(s['painPoint'])}',
      highlights: {hl_str},
      demoGoal: '{esc(s['demoGoal'])}',
      messages: [
        {msgs_str},
      ],
      annotations: [
        {anns_str},
      ],
    }},'''

