def s(id, stage, name, pain, highlights, goal, msgs, anns):
    scenarios.append({
        'id': id, 'stageId': stage, 'name': name,
        'painPoint': pain, 'highlights': highlights, 'demoGoal': goal,
        'messages': msgs, 'annotations': anns
    })

# Helper shortcuts
C = 'customer'
SL = 'sales'
AI = 'ai-hint'
AR = 'ai-recommend'
SY = 'system'

def sys(t): return f"sys('{t}')"
def cust(t): return f"customer('{t}')"
def sal(t): return f"sales('{t}')"
def hint(t): return f"aiHint('{t}')"
def rec(t, opts, sel): return f"aiRecommend('{t}',{opts},{sel})"

def ann(profile, progress, flows, cards, nextAction):
    flows_str = '[' + ','.join(f"{{label:'{f[0]}',icon:'{f[1]}'}}" for f in flows) + ']'
    cards_str = '[' + ','.join(f"'{c}'" for c in cards) + ']'
    na_str = f"{{suggestion:'{nextAction[0]}',reason:'{nextAction[1]}'}}"
    if len(nextAction) > 2:
        na_str = f"{{suggestion:'{nextAction[0]}',reason:'{nextAction[1]}',recommendedScript:'{nextAction[2]}'}}"
    return f"annot('{profile}',{progress},{flows_str},{cards_str},{na_str})"

