function buildFields(stageId, scenarioId) {
  const fields = {};
  const override = SCENARIO_OVERRIDES[scenarioId] || {};
  const fillList = STAGE_FILL[stageId] || STAGE_FILL.P1;

  const defaults = STAGE_DEFAULTS[stageId] || {};

  ALL_FIELDS.forEach(key => {
    const val = override[key] || defaults[key];
    const isInFill = fillList.includes(key);
    const isEmpty = !isInFill || !val;

    if (isEmpty) {
      fields[key] = { value: null, status: 'empty' };
    } else {
      fields[key] = { value: val, status: 'current' };
    }
  });

  // 强制设置当前阶段
  if (!fields['当前阶段'] || fields['当前阶段'].status === 'empty') {
    fields['当前阶段'] = { value: stageId, status: 'current' };
  }

  // 强制设置画像完整度（根据填充比例）
  const filledCount = ALL_FIELDS.filter(k => fields[k].status === 'current').length;
  const pct = Math.round((filledCount / ALL_FIELDS.length) * 100);
  fields['画像完整度'] = { value: pct + '%', status: 'current' };

  // 如果客户来源未填但 override 中有，则使用
  if (!fields['客户来源'] || fields['客户来源'].status === 'empty') {
    const src = STAGE_SOURCES[stageId];
    if (src) fields['客户来源'] = { value: src.value, status: 'current' };
  }

  return fields;
}

// 主流程
const files = readdirSync(DIR).filter(f => f.endsWith('.json') && f !== 'manifest.json');
let updated = 0;

files.forEach(f => {
  const fp = join(DIR, f);
  const data = JSON.parse(readFileSync(fp, 'utf-8'));
  const stageId = data.stageId;
  const scenarioId = data.id;

  if (!stageId) return;

  let changed = false;

  // 为每个非 null 的 annotation 补充 fields
  data.annotations.forEach((ann, idx) => {
    if (!ann) return;

    // 如果已经有关联的 agentLog，检查是否有 agentLog 中的 updates 字段
    const newFields = buildFields(stageId, scenarioId);

    if (ann.fields) {
      // 已有字段：检查是否有该填但未填的
      let needsUpdate = false;
      Object.keys(newFields).forEach(key => {
        const existing = ann.fields[key];
        if (!existing) {
          ann.fields[key] = newFields[key];
          needsUpdate = true;
        } else if (existing.status === 'empty' && newFields[key].status === 'current') {
          // 如果当前是 empty 但应该已填写，则补充
          // 但对 P1 场景不做强制覆盖（确实未知）
          if (stageId !== 'P1') {
            ann.fields[key] = { value: newFields[key].value, status: 'updated' };
            needsUpdate = true;
          }
        }
      });
      if (needsUpdate) changed = true;
    } else {
      // 没有字段：补充完整
      ann.fields = Object.assign({}, newFields);
      changed = true;
    }

    // 对 P4+ 场景：不在 override 中的字段也填充默认值
    if (['P4','P5','P6','P7'].includes(stageId) && ann.fields) {
      Object.keys(newFields).forEach(key => {
        const f = ann.fields[key];
        if (f && f.status === 'empty' && newFields[key].status === 'current') {
          ann.fields[key] = { value: newFields[key].value, status: 'current' };
          changed = true;
        }
      });
    }
  });

  if (changed) {
    writeFileSync(fp, JSON.stringify(data, null, 2) + '\n', 'utf-8');
    updated++;
    const annCount = data.annotations.filter(a => a !== null && a.fields).length;
    const firstFields = data.annotations.find(a => a && a.fields);
    const filled = firstFields ? Object.values(firstFields.fields).filter(v => v.status === 'current').length : 0;
    console.log(`  ${f.padEnd(10)} ${stageId}  annotations:${annCount}  filled:${filled}/${ALL_FIELDS.length}`);
  }
});

console.log(`\nDone. Updated ${updated} files.`);
